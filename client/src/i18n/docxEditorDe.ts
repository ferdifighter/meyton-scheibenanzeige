const DOCX_EDITOR_DE_MAP_LOWER = new Map(
  Object.entries(DOCX_EDITOR_DE_MAP).map(([k, v]) => [k.toLowerCase(), v])
);
const DOCX_EDITOR_DE_VALUES_LOWER = new Set(
  Object.values(DOCX_EDITOR_DE_MAP).map((v) => v.toLowerCase())
);

const isDev = typeof import.meta !== "undefined" && Boolean(import.meta.env?.DEV);
const missingTranslations = new Set<string>();
let missingLogTimer: number | null = null;

type MissingCategory =
  | "toolbar-format"
  | "toolbar-insert"
  | "layout-ruler"
  | "outline-comments"
  | "menus-history"
  | "dialogs"
  | "shortcuts"
  | "other";

const CATEGORY_ORDER: MissingCategory[] = [
  "toolbar-format",
  "toolbar-insert",
  "layout-ruler",
  "outline-comments",
  "menus-history",
  "dialogs",
  "shortcuts",
  "other",
];

const CATEGORY_LABELS: Record<MissingCategory, string> = {
  "toolbar-format": "Toolbar: Formatierung",
  "toolbar-insert": "Toolbar: Einfügen",
  "layout-ruler": "Layout/Lineale",
  "outline-comments": "Gliederung/Kommentare",
  "menus-history": "Menü/Verlauf",
  "dialogs": "Dialoge",
  "shortcuts": "Tastenkürzel",
  "other": "Sonstiges",
};

function categorizeMissing(value: string): MissingCategory {
  if (
    /\b(?:Bold|Italic|Underline|Strikethrough|Subscript|Superscript|font|Text formatting|Formatting toolbar|alignment|indent|spacing|style|format)\b/i.test(
      value
    )
  ) {
    return "toolbar-format";
  }
  if (/\b(?:Insert|Table|Image|Link|Symbol|Bullet|List)\b/i.test(value)) {
    return "toolbar-insert";
  }
  if (
    /\b(?:margin|ruler|Page Setup|orientation|portrait|landscape|Header|Footer|Page|Pages|Zoom)\b/i.test(
      value
    )
  ) {
    return "layout-ruler";
  }
  if (/\b(?:outline|heading|comment|Notifications)\b/i.test(value)) {
    return "outline-comments";
  }
  if (/\b(?:File|Format|History|Menu)\b/i.test(value)) {
    return "menus-history";
  }
  if (/\b(?:Dialog|Properties|Select)\b/i.test(value)) {
    return "dialogs";
  }
  if (/\b(?:Ctrl|Alt|Shift)\b/i.test(value)) {
    return "shortcuts";
  }
  return "other";
}

function getGroupedMissingList(): Record<MissingCategory, string[]> {
  const grouped: Record<MissingCategory, string[]> = {
    "toolbar-format": [],
    "toolbar-insert": [],
    "layout-ruler": [],
    "outline-comments": [],
    "menus-history": [],
    "dialogs": [],
    "shortcuts": [],
    "other": [],
  };
  for (const value of missingTranslations) {
    grouped[categorizeMissing(value)].push(value);
  }
  for (const k of CATEGORY_ORDER) {
    grouped[k].sort((a, b) => a.localeCompare(b, "de"));
  }
  return grouped;
}

function collectMissingTranslation(input: string): void {
  if (!isDev) return;
  const value = input.trim();
  if (!value) return;
  if (value.length < 3 || value.length > 80) return;
  if (value.includes("{") || value.includes("}")) return;
  if (
    value === "Aptos" ||
    value === "Arial" ||
    value === "Damenpokal" ||
    value === "Editor wird geladen…"
  ) {
    return;
  }
  if (/^[A-Za-z]+\.docx$/i.test(value)) return;
  if (/\b(?:In der Disziplin|belegte|mit|den)\b/i.test(value)) return;
  if (/[äöüÄÖÜß]/.test(value)) return;
  if (!/[A-Za-z]/.test(value)) return;
  if (/^[0-9\s().,:%/-]+$/.test(value)) return;
  if (
    !/\b(?:[A-Z][a-z]+|Ctrl|Alt|Shift|Zoom|margin|indent|toolbar|outline|spacing|font|script|history|notifications|list|format)\b/i.test(
      value
    )
  ) {
    return;
  }
  if (DOCX_EDITOR_DE_MAP[value] || DOCX_EDITOR_DE_MAP_LOWER.get(value.toLowerCase())) return;
  if (DOCX_EDITOR_DE_VALUES_LOWER.has(value.toLowerCase())) return;
  missingTranslations.add(value);
  const w = globalThis as typeof globalThis & {
    __docxEditorMissingTranslations?: string[];
    __docxEditorMissingTranslationsByCategory?: Record<MissingCategory, string[]>;
  };
  w.__docxEditorMissingTranslations = [...missingTranslations].sort((a, b) =>
    a.localeCompare(b, "de")
  );
  w.__docxEditorMissingTranslationsByCategory = getGroupedMissingList();
  if (missingLogTimer != null) return;
  missingLogTimer = window.setTimeout(() => {
    missingLogTimer = null;
    const list = [...missingTranslations].sort((a, b) => a.localeCompare(b, "de"));
    if (list.length === 0) return;
    const grouped = getGroupedMissingList();
    console.groupCollapsed(`[docxEditorDe] Nicht übersetzte Begriffe (${list.length})`);
    for (const category of CATEGORY_ORDER) {
      const entries = grouped[category];
      if (entries.length === 0) continue;
      console.groupCollapsed(`${CATEGORY_LABELS[category]} (${entries.length})`);
      console.table(entries);
      console.groupEnd();
    }
    console.log(
      "Tipp: Begriffe in client/src/i18n/docxEditorDe.ts zu DOCX_EDITOR_DE_MAP hinzufügen."
    );
    console.groupEnd();
  }, 600);
}

function replaceDynamicText(input: string): string | null {
  const value = input.trim();
  let m = value.match(/^All \((\d+) pages\)$/i);
  if (m) return `Alle (${m[1]} Seiten)`;
  m = value.match(/^Zoom:\s*(\d+)%$/i);
  if (m) return `Zoom: ${m[1]}%`;
  m = value.match(/^Page (\d+)$/i);
  if (m) return `Seite ${m[1]}`;
  m = value.match(/^Pages (\d+)-(\d+)$/i);
  if (m) return `Seiten ${m[1]}-${m[2]}`;
  m = value.match(/^(\d+)\s+pages$/i);
  if (m) return `${m[1]} Seiten`;
  return null;
}

function replaceExactText(input: string): string {
  const dyn = replaceDynamicText(input);
  if (dyn) return dyn;
  const direct = DOCX_EDITOR_DE_MAP[input];
  if (direct) return direct;
  const trimmed = input.trim();
  const mapped = DOCX_EDITOR_DE_MAP[trimmed];
  const mappedLower = DOCX_EDITOR_DE_MAP_LOWER.get(trimmed.toLowerCase());
  const next = mapped || mappedLower;
  if (!next) {
    collectMissingTranslation(input);
    return input;
  }
  if (trimmed === input) return next;
  return input.replace(trimmed, next);
}

function translateElementTree(root: HTMLElement): void {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const textNodes: Text[] = [];
  while (walker.nextNode()) {
    const node = walker.currentNode;
    if (node.nodeType === Node.TEXT_NODE) textNodes.push(node as Text);
  }
  for (const n of textNodes) {
    const current = n.nodeValue ?? "";
    const next = replaceExactText(current);
    if (next !== current) n.nodeValue = next;
  }
  const elements = root.querySelectorAll<HTMLElement>("*");
  for (const el of elements) {
    const title = el.getAttribute("title");
    if (title != null) {
      const next = replaceExactText(title);
      if (next !== title) el.setAttribute("title", next);
    }
    const ariaLabel = el.getAttribute("aria-label");
    if (ariaLabel != null) {
      const next = replaceExactText(ariaLabel);
      if (next !== ariaLabel) el.setAttribute("aria-label", next);
    }
    const placeholder = el.getAttribute("placeholder");
    if (placeholder != null) {
      const next = replaceExactText(placeholder);
      if (next !== placeholder) el.setAttribute("placeholder", next);
    }
  }
}

export function mountDocxEditorGerman(root: HTMLElement): () => void {
  let stopped = false;
  let translating = false;
  let scheduled = false;
  const run = () => {
    if (stopped || translating) return;
    translating = true;
    try {
      translateElementTree(root);
    } finally {
      translating = false;
    }
  };
  const scheduleRun = () => {
    if (stopped || scheduled) return;
    scheduled = true;
    window.setTimeout(() => {
      scheduled = false;
      run();
    }, 120);
  };
  run();
  const observer = new MutationObserver(() => {
    if (translating) return;
    scheduleRun();
  });
  observer.observe(root, {
    subtree: true,
    childList: true,
    characterData: true,
  });
  return () => {
    stopped = true;
    observer.disconnect();
  };
}
